# Contract 4: gen_coplay runner-to-source-dir derivation rule
# ---------------------------------------------------------------------------

class TestRunnerToSourceDirDerivation:
    """Verify the derivation logic in gen_coplay.main() without actually
    executing the CLI.  We reproduce the derivation rule from the source and
    assert it produces the expected paths for canonical and non-canonical
    runner names.

    The rule (from gen_coplay.py):
        stem = runner_path.stem
        derived = runner_path.parent / (
            stem[4:] + "_python" if stem.startswith("run_") else f"{stem}_python"
        )
    """

    def _derive(self, runner_path: Path) -> Path:
        """Reproduce the derivation logic from gen_coplay.py."""
        stem = runner_path.stem
        return runner_path.parent / (
            stem[4:] + "_python" if stem.startswith("run_") else f"{stem}_python"
        )

    def test_run_game_derives_game_python(self, tmp_path):
        """run_flappy_bird.py → flappy_bird_python/ (strip 'run_' prefix)."""
        runner = tmp_path / "run_flappy_bird.py"
        runner.touch()
        derived = self._derive(runner)
        assert derived.name == "flappy_bird_python"

    def test_run_pacman_derives_pacman_python(self, tmp_path):
        """run_pacman_v2.py → pacman_v2_python/"""
        runner = tmp_path / "run_pacman_v2.py"
        runner.touch()
        derived = self._derive(runner)
        assert derived.name == "pacman_v2_python"

    def test_plain_name_appends_python(self, tmp_path):
        """game.py (no run_ prefix) → game_python/"""
        runner = tmp_path / "game.py"
        runner.touch()
        derived = self._derive(runner)
        assert derived.name == "game_python"

    def test_derived_dir_used_when_exists(self, tmp_path):
        """When the derived directory exists the helper is called; when it
        does not exist the filter is disabled (translated_classes stays None).
        This validates that gen_coplay gates on is_dir() before calling the
        helper."""
        runner = tmp_path / "run_test_game.py"
        runner.touch()
        derived = self._derive(runner)
        # Dir does not exist yet → filter disabled
        assert not derived.is_dir()
        # Create the dir → filter would be enabled
        derived.mkdir()
        assert derived.is_dir()

    def test_derivation_is_sibling_of_runner(self, tmp_path):
        """Derived directory is a sibling of the runner file, not a child."""
        runner = tmp_path / "examples" / "mygame" / "run_mygame.py"
        runner.parent.mkdir(parents=True, exist_ok=True)
        runner.touch()
        derived = self._derive(runner)
        assert derived.parent == runner.parent

    def test_run_prefix_exactly_four_chars_stripped(self, tmp_path):
        """Only the literal 'run_' prefix (4 chars) is stripped."""
        # 'runner_game.py' starts with 'run' but not 'run_' — should append _python
        runner = tmp_path / "runner_game.py"
        runner.touch()
        derived = self._derive(runner)
        # Does NOT start with 'run_', so rule is: stem + '_python'
        assert derived.name == "runner_game_python"
