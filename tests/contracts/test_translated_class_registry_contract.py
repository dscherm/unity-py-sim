# ---------------------------------------------------------------------------

class TestSerializeSceneBackCompat:

    def test_no_kwarg_emits_all_monobehaviours(self):
        """serialize_scene() without translated_classes must emit every MonoBehaviour."""
        go = GameObject("All")
        go.add_component(TranslatedBehaviour)
        go.add_component(InlineBehaviour)

        result = serialize_scene()
        go_data = result["game_objects"][0]
        types = [c["type"] for c in go_data["components"]]
        assert "TranslatedBehaviour" in types
        assert "InlineBehaviour" in types

    def test_none_kwarg_emits_all_monobehaviours(self):
        """serialize_scene(translated_classes=None) must be equivalent to no kwarg."""
        go = GameObject("AllNone")
        go.add_component(TranslatedBehaviour)
        go.add_component(InlineBehaviour)

        result = serialize_scene(translated_classes=None)
        go_data = result["game_objects"][0]
        types = [c["type"] for c in go_data["components"]]
        assert "TranslatedBehaviour" in types
        assert "InlineBehaviour" in types

    def test_back_compat_does_not_raise(self):
        """No exception when calling serialize_scene() without the kwarg on an
        existing scene that was working before FU-1."""
        go = GameObject("Safe")
        go.add_component(TranslatedBehaviour)
        # Must not raise
        result = serialize_scene()
        assert len(result["game_objects"]) == 1


