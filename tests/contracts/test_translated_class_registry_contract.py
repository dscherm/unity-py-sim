class TestGetTranslatedClassNames:

    def test_returns_empty_set_for_missing_directory(self):
        """Contract: missing directory must return an empty set, not raise."""
        result = get_translated_class_names("/does/not/exist/at/all")
        assert result == set()

    def test_returns_empty_set_for_empty_directory(self, tmp_path):
        """Contract: a directory with no .py files returns empty set."""
        result = get_translated_class_names(tmp_path)
        assert result == set()

    def test_ignores_init_py(self, tmp_path):
        """Contract: __init__.py must never contribute class names."""
        init = tmp_path / "__init__.py"
        init.write_text(
            textwrap.dedent("""\
                from src.engine.core import MonoBehaviour

                class InitClass(MonoBehaviour):
                    pass
            """),
            encoding="utf-8",
        )
        result = get_translated_class_names(tmp_path)
        assert "InitClass" not in result

    def test_collects_monobehaviour_subclass(self, tmp_path):
        """Contract: a class that inherits MonoBehaviour must appear in the result."""
        (tmp_path / "player.py").write_text(
            textwrap.dedent("""\
                from src.engine.core import MonoBehaviour

                class Player(MonoBehaviour):
                    pass
            """),
            encoding="utf-8",
        )
        result = get_translated_class_names(tmp_path)
        assert "Player" in result

    def test_excludes_non_monobehaviour_class(self, tmp_path):
        """Contract: plain Python classes not inheriting MonoBehaviour are excluded."""
        (tmp_path / "utils.py").write_text(
            textwrap.dedent("""\
                class HelperUtil:
                    pass
            """),
            encoding="utf-8",
        )
        result = get_translated_class_names(tmp_path)
        assert "HelperUtil" not in result

    def test_collects_multiple_classes_across_files(self, tmp_path):
        """Contract: multiple MonoBehaviour classes across different files are all collected."""
        (tmp_path / "player.py").write_text(
            textwrap.dedent("""\
                from src.engine.core import MonoBehaviour

                class Player(MonoBehaviour):
                    pass
            """),
            encoding="utf-8",
        )
        (tmp_path / "enemy.py").write_text(
            textwrap.dedent("""\
                from src.engine.core import MonoBehaviour

                class Enemy(MonoBehaviour):
                    pass
            """),
            encoding="utf-8",
        )
        result = get_translated_class_names(tmp_path)
        assert "Player" in result
        assert "Enemy" in result

    def test_only_scans_direct_children_not_subdirectories(self, tmp_path):
        """Contract: only *.py directly in directory — not recursive."""
        subdir = tmp_path / "subpkg"
        subdir.mkdir()
        (subdir / "nested.py").write_text(
            textwrap.dedent("""\
                from src.engine.core import MonoBehaviour

                class NestedClass(MonoBehaviour):
                    pass
            """),
            encoding="utf-8",
        )
        result = get_translated_class_names(tmp_path)
        assert "NestedClass" not in result

    def test_returns_set_not_list(self, tmp_path):
        """Contract: return type is set[str]."""
        result = get_translated_class_names(tmp_path)
        assert isinstance(result, set)

    def test_multiple_classes_same_file_all_collected(self, tmp_path):
        """Contract: a single file with two MonoBehaviour subclasses emits both names."""
        (tmp_path / "controllers.py").write_text(
            textwrap.dedent("""\
                from src.engine.core import MonoBehaviour

                class BallController(MonoBehaviour):
                    pass

                class PaddleController(MonoBehaviour):
                    pass
            """),
            encoding="utf-8",
        )
        result = get_translated_class_names(tmp_path)
        assert "BallController" in result
        assert "PaddleController" in result


