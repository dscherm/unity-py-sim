class TestSerializeSceneWithFilter:

    def test_translated_component_is_kept(self):
        """A MonoBehaviour whose class is in translated_classes must appear in output."""
        go = GameObject("Player")
        go.add_component(TranslatedBehaviour)

        result = serialize_scene(translated_classes={"TranslatedBehaviour"})
        go_data = result["game_objects"][0]
        types = [c["type"] for c in go_data["components"]]
        assert "TranslatedBehaviour" in types

    def test_inline_component_is_dropped(self):
        """A MonoBehaviour whose class is NOT in translated_classes must be dropped."""
        go = GameObject("Runner")
        go.add_component(InlineBehaviour)

        result = serialize_scene(translated_classes={"TranslatedBehaviour"})
        go_data = result["game_objects"][0]
        types = [c["type"] for c in go_data["components"]]
        assert "InlineBehaviour" not in types

    def test_gameobject_still_present_after_all_monobehaviours_dropped(self):
        """The contract states: GameObjects are never dropped, only their user components.
        A GO whose only MonoBehaviour is filtered must still appear in game_objects."""
        go = GameObject("InlineOnly")
        go.add_component(InlineBehaviour)

        result = serialize_scene(translated_classes={"TranslatedBehaviour"})
        names = [g["name"] for g in result["game_objects"]]
        assert "InlineOnly" in names

    def test_two_gos_mixed_components_correct_filtering(self):
        """Two GameObjects, one with a translated component and one with only inline.
        After filter: both GOs present, translated component kept, inline dropped."""
        go_a = GameObject("Alpha")
        go_a.add_component(TranslatedBehaviour)

        go_b = GameObject("Beta")
        go_b.add_component(InlineBehaviour)

        result = serialize_scene(translated_classes={"TranslatedBehaviour"})

        # Both GOs must be in the output
        names = {g["name"] for g in result["game_objects"]}
        assert "Alpha" in names
        assert "Beta" in names

        # Alpha's translated component kept
        alpha_data = next(g for g in result["game_objects"] if g["name"] == "Alpha")
        alpha_types = [c["type"] for c in alpha_data["components"]]
        assert "TranslatedBehaviour" in alpha_types

        # Beta's inline component dropped
        beta_data = next(g for g in result["game_objects"] if g["name"] == "Beta")
        beta_types = [c["type"] for c in beta_data["components"]]
        assert "InlineBehaviour" not in beta_types

    def test_engine_primitives_never_dropped_by_filter(self):
        """Transform, SpriteRenderer, etc. must NOT be filtered regardless of
        translated_classes — they are not MonoBehaviours."""
        go = GameObject("Sprite")
        sr = go.add_component(SpriteRenderer)
        sr.asset_ref = "player_sprite"

        # Pass an empty translated_classes set — would drop all MonoBehaviours
        result = serialize_scene(translated_classes=set())
        go_data = result["game_objects"][0]
        types = [c["type"] for c in go_data["components"]]
        assert "SpriteRenderer" in types

    def test_empty_translated_classes_drops_all_monobehaviours(self):
        """An empty set means no MonoBehaviour survives the filter."""
        go = GameObject("Test")
        go.add_component(TranslatedBehaviour)
        go.add_component(InlineBehaviour)

        result = serialize_scene(translated_classes=set())
        go_data = result["game_objects"][0]
        mb_types = [c["type"] for c in go_data["components"]
                    if c.get("is_monobehaviour")]
        assert mb_types == []


